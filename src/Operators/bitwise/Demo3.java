package Operators.bitwise;

public class Demo3 {
    public static void main(String[] args) {
        System.out.println("Logical Negation  - ");
        System.out.println(~4); // -5  (input+1 and change the sign)
        System.out.println(~-5); // -5+1 = -4 ---> 4
        System.out.println(~6); // -7
    }
}
